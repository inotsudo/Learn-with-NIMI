interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  // Always start with "rw" so the client's first hydration pass matches the
  // server-rendered HTML exactly; the real stored language (if any) is
  // applied in the effect below, after hydration completes.
  const [language, setLanguage] = useState<Language>("rw");

  useEffect(() => {
    const stored = localStorage.getItem(LANGUAGE_KEY) as Language | null;
    if (stored && stored !== "rw") setLanguage(stored);

    const onStorage = (e: StorageEvent) => {
      if (e.key === LANGUAGE_KEY && e.newValue) {
        setLanguage(e.newValue as Language);
      }
    };
    
    window.addEventListener("storage", onStorage);
    
    const onCustom = (e: Event) => {
      const detail = (e as CustomEvent).detail as { language: Language };
      if (detail?.language) {
        localStorage.setItem(LANGUAGE_KEY, detail.language);
        setLanguage(detail.language);
      }
    };
    
    window.addEventListener("app:languageChange", onCustom as EventListener);
    
    return () => {
      window.removeEventListener("storage", onStorage);
      window.removeEventListener("app:languageChange", onCustom as EventListener);
    };
  }, []);

  const value = {
    language,
    setLanguage: (lang: Language) => {
      localStorage.setItem(LANGUAGE_KEY, lang);
      setLanguage(lang);
      window.dispatchEvent(
        new CustomEvent("app:languageChange", { detail: { language: lang } })
      );
    },
    t: (key: string) => {
      const table = dict[language] || dict.rw;
      return table[key] ?? key;
    }
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}